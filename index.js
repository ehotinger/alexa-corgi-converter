const Alexa = require("alexa-sdk");
const appId = "<YOUR ALEXA APP ID>";

exports.handler = function (event, context, callback) {
    const alexa = Alexa.handler(event, context);
    alexa.appId = appId;
    alexa.registerHandlers(handlers);
    alexa.execute();
};

const handlers = {
    'LaunchRequest': function () {
        console.log('[LaunchRequest]');
        this.response
            .speak('Welcome to Corgi Converter. You can ask how much animals weigh.')
            .listen('Ask how much does a baby elephant weigh');
        this.emit(':responseReady');
    },
    'SessionEndedRequest': function () {
        console.log('[SessionEndedRequest]');
        this.emit(':responseReady');
    },
    'GetWeightIntent': function () {
        console.log('[GetWeightIntent]');

        var input = this.event.request.intent.slots.input.value;
        console.log('User said: ' + input);

        // Alexa is sometimes bad at parsing animal names. 
        // It can insert things like "Ask Alexa" as an animal name.
        input = input.replace("Ask ", "")
            .replace("Ask", "")
            .replace("ask", "")
            .replace("ask ", "")
            .replace("Alexa ", "")
            .replace("Alexa", "")
            .toLowerCase()

        if (input) {
            var firstLetter = input[0];
            var junction = "a ";
            if (firstLetter && (firstLetter === 'a' || firstLetter === 'e' || firstLetter === 'i' || firstLetter === 'o' || firstLetter === 'u')) {
                junction = "an ";
            }

            if (animals[input.toLowerCase()]) {
                var corgiWeight = 27;
                var weight = Math.round(animals[input.toLowerCase()] * 2.20462 / corgiWeight);
                this.response.speak(junction + input.toLowerCase() + " weighs about " + weight + " corgis");
            }
            else {
                var speechOutput = "Corgi Converter doesn't know the weight of " + junction + input;
                this.response.speak(speechOutput);
            }
        } else {
            console.log('Input was undefined. Emitting Unhandled.');
            this.emit('Unhandled')
        }
        this.emit(':responseReady')
    },
    'AMAZON.HelpIntent': function () {
        console.log('[HelpIntent]');
        this.response
            .speak('This is the Corgi Converter skill. You can ask how much animals weigh.')
            .listen('Ask how much does a baby elephant weigh.');
        this.emit(':responseReady');
    },
    'AMAZON.CancelIntent': function () {
        console.log('[CancelIntent]');
        this.response.speak('Corgi Converter says bye. Bark Bark!');
        this.emit(':responseReady');
    },
    'AMAZON.StopIntent': function () {
        console.log('[StopIntent]');
        this.response.speak('Corgi Converter says bye. Woof Woof!');
        this.emit(':responseReady');
    },
    'Unhandled': function () {
        console.log("[Unhandled]");
        this.response.speak("Sorry, I didn't catch that. Try asking how much a baby elephant weighs.")
            .listen('Try asking how much an animal weighs.');
        this.emit(':responseReady');
    }
};

